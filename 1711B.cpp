void solve()
{
    ll n,pairs; cin>>n>>pairs;
    vector<ll>nums(n+1);
    for(ll i=1;i<=n;i++)
        cin>>nums[i];
    

    // Form the adjacency list
    vector<ll>deg(n+1, 0);
    vector<ll>adj[n+1];
    for(ll i=1;i<=pairs;i++)
    {
        ll u, v; cin>>u>>v;
        adj[u].push_back(v);
        adj[v].push_back(u);
        deg[u]++;
        deg[v]++;
    }

    if(!(pairs&1)){
        cout<<0<<endl;
        return;
    }

    // Two cases exists --> Either you remove the odd connected member or two even connected member provided the two members should also be connected with each other.

    ll s1 = 1e9, s2 = 1e9;
    for(ll i=1;i<=n;i++)
    {
        ll d = deg[i];
        if(d&1)
            s1 = min(s1, nums[i]);
        else{
            for(ll j=0;j<d;j++)
            {
                if(!(deg[adj[i][j]]&1))
                    s2 = min(s2, nums[i] + nums[adj[i][j]]);
            }
        }
    }
    cout<<min(s1, s2)<<endl;
}
