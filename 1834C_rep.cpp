ll logic(string& s, string& t, bool f)
{
    ll n = s.length();
    ll c = 0;
    for(ll i=0;i<n;i++)
    {   
        if(s[i] != t[i])
            c++;
    }   
    if(f)
    {
        if(c&1)
            return 2*c-1;
        return 2*c; 
    }
    else
    {
        if(c&1)
            return 2*c;
        return (2*c-1 > 0) ? 2*c-1 : 2;
    }
}

