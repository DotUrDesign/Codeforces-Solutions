bool check(ll mid, vector<ar<ll,2>>& maxi)
{
    for(auto it : maxi){
        if(mid <= it[0])
            return false;
        mid += it[1];
    }
    return true;
}

void solve()
{
    ll n; cin>>n;
    vector<ar<ll,2>>maxi;
    ll tmp = n;
    while(tmp--){
        ll size; cin>>size;
        ll maxn = INT_MIN;
        for(ll i=0;i<size;i++){
            ll k; cin>>k;
            maxn = max(maxn, k-i);
        }
        maxi.push_back({maxn, size});
    }
    sort(maxi.begin(), maxi.end());
    // for(auto it : maxi)
    //     cout<<it[0]<<" "<<it[1]<<endl;
    // cout<<endl;

    ll low = 0;
    ll high = 1e18;
    ll ans = low;
    while(low <= high)
    {
        ll mid = low + (high - low)/2;
        if(check(mid, maxi)){
            ans = mid;
            high = mid-1;
        }
        else{
            low = mid+1;
        }
    }
    cout<<ans<<endl;
}
